RCON = [0x00,0x01,0x02,0x04,0x08,0x10,0x20,0x40,0x80,0x1b,0x36]
# Constantes usadas na expansão da chave (Key Expansion)

BLOCK_SIZE = 16  # bloco de 16 bytes (128 bits)

# Substitui cada byte usando a S-BOX
def sub_bytes(s):
    return [S_BOX[b] for b in s]

# Faz o processo inverso da substituição
def inv_sub_bytes(s):
    return [S_BOX_INV[b] for b in s]

# Desloca as linhas da matriz (representada linearmente)
def shift_rows(s):
    return [
        s[0], s[5], s[10], s[15],
        s[4], s[9], s[14], s[3],
        s[8], s[13], s[2], s[7],
        s[12], s[1], s[6], s[11]
    ]

# Deslocamento inverso das linhas
def inv_shift_rows(s):
    return [
        s[0], s[13], s[10], s[7],
        s[4], s[1], s[14], s[11],
        s[8], s[5], s[2], s[15],
        s[12], s[9], s[6], s[3]
    ]

# Multiplicação no campo de Galois GF(2^8)
# usada no MixColumns
def gmul(a, b):
    p = 0
    for _ in range(8):
        if b & 1:
            p ^= a   # XOR (adição no campo)
        carry = a & 0x80  # verifica overflow
        a = (a << 1) & 0xFF  # desloca
        if carry:
            a ^= 0x1B  # redução modular
        b >>= 1
    return p

# Mistura os bytes de cada coluna
def mix_columns(s):
    for i in range(0,16,4):
        a = s[i:i+4]
        s[i]   = gmul(2,a[0]) ^ gmul(3,a[1]) ^ a[2] ^ a[3]
        s[i+1] = a[0] ^ gmul(2,a[1]) ^ gmul(3,a[2]) ^ a[3]
        s[i+2] = a[0] ^ a[1] ^ gmul(2,a[2]) ^ gmul(3,a[3])
        s[i+3] = gmul(3,a[0]) ^ a[1] ^ a[2] ^ gmul(2,a[3])
    return s

# Versão inversa do MixColumns
def inv_mix_columns(s):
    for i in range(0,16,4):
        a = s[i:i+4]
        s[i]   = gmul(0x0E,a[0]) ^ gmul(0x0B,a[1]) ^ gmul(0x0D,a[2]) ^ gmul(0x09,a[3])
        s[i+1] = gmul(0x09,a[0]) ^ gmul(0x0E,a[1]) ^ gmul(0x0B,a[2]) ^ gmul(0x0D,a[3])
        s[i+2] = gmul(0x0D,a[0]) ^ gmul(0x09,a[1]) ^ gmul(0x0E,a[2]) ^ gmul(0x0B,a[3])
        s[i+3] = gmul(0x0B,a[0]) ^ gmul(0x0D,a[1]) ^ gmul(0x09,a[2]) ^ gmul(0x0E,a[3])
    return s

# XOR entre estado e chave da rodada
def add_round_key(s, k):
    return [a ^ b for a, b in zip(s, k)]

# KEY EXPANSION
def key_expansion(key):
    # Divide a chave em palavras de 4 bytes
    words = [list(key[i:i+4]) for i in range(0,32,4)]

    for i in range(8,60):
        temp = words[i-1][:] # pega a última palavra

        if i % 8 == 0:
            temp = temp[1:] + temp[:1]  # RotWord - rotaciona 1 byte
            temp = [S_BOX[b] for b in temp]  # SubWord - aplica S-BOX
            temp[0] ^= RCON[i//8]  # XOR com RCON

        elif i % 8 == 4:
            temp = [S_BOX[b] for b in temp]

        # Gera nova palavra
        words.append([a ^ b for a, b in zip(words[i-8], temp)])

    # Agrupa palavras em chaves de rodada (16 bytes)
    return [sum(words[i:i+4], []) for i in range(0,60,4)][:15]

# PBKDF2 para derivar chave a partir da senha
def derivar_chave(senha, salt):
    # Converte senha em chave forte
    return hashlib.pbkdf2_hmac(
        'sha256',
        senha.encode(),
        salt,
        100000, # 100 mil iterações de hash
        dklen=32 # saída = 32 bytes = 256 bits
    )

# PADDING
def pad(data): 
    # PKCS7 → adiciona bytes extras
    p = BLOCK_SIZE - len(data) % BLOCK_SIZE
    return data + bytes([p]*p)

def unpad(data):
    p = data[-1]
    if p < 1 or p > 16:
        raise ValueError("Padding inválido")
    if data[-p:] != bytes([p]*p):
        raise ValueError("Padding inválido")
    return data[:-p]

# CBC (Cipher Block Chaining)
def xor_bytes(a, b):
    # XOR byte a byte
    return bytes(x ^ y for x, y in zip(a, b))

# Gerencia o processo de cifragem em modo CBC
def encrypt_cbc(data, key, iv):
    keys = key_expansion(key)
    data = pad(data)
    out = b''
    prev = iv  # primeiro bloco usa IV

    for i in range(0, len(data), 16):
        block = data[i:i+16]

        block = xor_bytes(block, prev)  # encadeamento CBC
        block = encrypt_block(block, keys)

        out += block
        prev = block  # próximo usa o bloco cifrado

    return out

def decrypt_cbc(data, key, iv):
    keys = key_expansion(key)
    out = b''
    prev = iv

    for i in range(0, len(data), 16):
        block = data[i:i+16]

        dec = decrypt_block(block, keys)
        dec = xor_bytes(dec, prev)

        out += dec
        prev = block

    return unpad(out)

# BLOCO AES (16 bytes)
def encrypt_block(block, keys):
    s = list(block)

    s = add_round_key(s, keys[0]) # Rodada 0 (inicial)

    for i in range(1,14):
        s = sub_bytes(s)
        s = shift_rows(s)
        s = mix_columns(s)
        s = add_round_key(s, keys[i]) # Rodadas 1-13

    s = sub_bytes(s)
    s = shift_rows(s)
    s = add_round_key(s, keys[14]) # Rodada final (sem MixColumns)

    return bytes(s)

def decrypt_block(block, keys):
    s = list(block)

    s = add_round_key(s, keys[14])

    for i in range(13,0,-1):
        s = inv_shift_rows(s)
        s = inv_sub_bytes(s)
        s = add_round_key(s, keys[i])
        s = inv_mix_columns(s)

    s = inv_shift_rows(s)
    s = inv_sub_bytes(s)
    s = add_round_key(s, keys[0])

    return bytes(s)

# CLI
def cifrar(arquivo):
    senha = input("Senha: ")

    salt = os.urandom(16)  # gera salt aleatório
    iv = os.urandom(16)    # gera IV aleatório

    key = derivar_chave(senha, salt)

    with open(arquivo, 'rb') as f:
        dados = f.read()

    cifrado = encrypt_cbc(dados, key, iv)

    with open(arquivo + ".cifrado", 'wb') as f:
        f.write(salt + iv + cifrado)

    print("Arquivo cifrado com sucesso! Salvo como:", arquivo + ".cifrado")

def decifrar(arquivo):
    senha = input("Senha: ")

    with open(arquivo, 'rb') as f:
        dados = f.read()

    salt = dados[:16]
    iv = dados[16:32]
    cifrado = dados[32:]

    key = derivar_chave(senha, salt)

    texto = decrypt_cbc(cifrado, key, iv)

    try:
        print(texto.decode())
    except:
        print("Erro ao decodificar (possível senha incorreta)")

